int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	validate;

	i = 0;
	validate = 0;
	if (str[i] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if (str[i] >= 65 && str[i] <= 90)
			validate = 1;
		else
		{	
			validate = 0;
			break ;
		}
		i++;
	}
	return (validate);
}
